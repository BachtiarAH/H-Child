    private void btn_simpanPasienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_simpanPasienMouseClicked
        // TODO add your handling code here:
       String namaAnak = namaAnak_txt.getText();
       String namaIbu = namaIbu_txt.getText();
       String tempatLahir = ttlPasien_txt.getText();
       int jenisKelamin = jenisKelaminPasien_combo.getSelectedIndex();
       java.util.Date dtBeforeConvert = jDate_tanggalLahirPasienTxt.getDate();
       String tinggi = tinggi_badan_txt.getText();
       String umur = umur_txt.getText();
        System.out.println(tinggi.equals(""));
        System.out.println(dtBeforeConvert);
        System.out.println(jenisKelamin);
        System.out.println(tempatLahir);
        System.out.println(tinggi);
        
       if(!namaAnak.equals("") && !namaIbu.equals("") && !tempatLahir.equals("") && jenisKelamin != 0 && dtBeforeConvert != null && !tinggi.equals("") && !umur.equals("")){
           try {
                int tinggiInt = Integer.parseInt(tinggi);
                int umurInt = Integer.parseInt(umur);
                java.sql.Date dt = new java.sql.Date(dtBeforeConvert.getDate());
                if(jenisKelamin == 0){
                    JOptionPane.showMessageDialog(null, "Harap Pilih Jenis Kelamin Terlebih Dahulu", "Terjadi Kesalahan", JOptionPane.INFORMATION_MESSAGE);
                }else{
                    String jkValue = "";
                    if(jenisKelamin == 1){
                        jkValue = "Laki-laki";
                    }else{
                        jkValue = "Perempuan";
                    }
                   boolean isAdd =  this.service.addPasien(namaAnak, namaIbu, tempatLahir,jkValue , dt, tinggiInt , umurInt);
                   if(isAdd){
                        JOptionPane.showMessageDialog(null, "Berhasil Menambahkan data Passien", "Success", JOptionPane.INFORMATION_MESSAGE);
                   }else{
                       JOptionPane.showMessageDialog(null, "Gagal Menambahkan data Passien", "failed", JOptionPane.INFORMATION_MESSAGE);
                   }
                }
           } catch (NumberFormatException e) {
               System.out.println(umur);
               System.out.println(tinggi);
               JOptionPane.showMessageDialog(null, "Gagal Menambahkan data Passien , harap masukan angka pada tinggi atau umur", "failed", JOptionPane.INFORMATION_MESSAGE);
           }
           
       }else{
           JOptionPane.showMessageDialog(null, "Harap Isi semua field", "Terjadi Kesalahan", JOptionPane.INFORMATION_MESSAGE);
       } 
    }//GEN-LAST:event_btn_simpanPasienMouseClicked

