    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        Date date = jDate_tanggalLahirPasienTxt.getDate();
       SimpleDateFormat formater = new SimpleDateFormat("yyyy-MM-dd");
       Calendar calendar = Calendar.getInstance();
       Date dtnow = calendar.getTime();
       String dateLahir =  formater.format(date);
       String dateNow = formater.format(dtnow);
        try {
            if(dateLahir.compareTo(dateNow) > 0){
            JOptionPane.showMessageDialog(null, "Harap Masukan tanggal lahir dengan benar", "information", JOptionPane.INFORMATION_MESSAGE);
        }else if(dateLahir.compareTo(dateNow) == 0){
                        JOptionPane.showMessageDialog(null, "Harap Masukan tanggal lampau", "information", JOptionPane.INFORMATION_MESSAGE);

        }
            
        else{
              if(date == null){
            JOptionPane.showMessageDialog(null, "Harap Masukan tanggl lahir terlebih dahulu", "information", JOptionPane.INFORMATION_MESSAGE);
        }else{
            int umur = this.service.getAge(date);
            System.out.println(umur);
            umur_txt.setText(String.valueOf(umur));
        }
        }
        } catch (NullPointerException e) {
                        JOptionPane.showMessageDialog(null, "Harap pilih tanggal lahir terlebih dahulu", "information", JOptionPane.INFORMATION_MESSAGE);
        }
      
    }//GEN-LAST:event_jButton1MouseClicked

